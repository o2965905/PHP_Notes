<h3>方法2:使用PHP</h3>
<?php
echo "<table>";
for($i=1;$i<=9;$i++){
//<tr>會重複9次,直接使用for迴圈
echo "<tr>";
    for($j=1;$j<=9;$j++){
        //<td>會重複9次,直接使用for迴圈
        echo "<td>{$i}x{$j}=";
        echo $i*$j;
        echo "</td>";
    }
echo "</tr>";
}
echo "</table>";
?>